public class VariableTest {
	public static void main(String[] args) {
		
		//변수(variable)
		int a = 3;	//변수선언, 초기ㅗ하
		int b = 7;	
		System.out.println("a : " + a);
		System.out.println("a : " + b);
		
		// 재할당
		a = 5;
		System.out.println("a :" + a);
		System.out.println("a :" + b);
		
		// 상수(constant)
		final int NUM = 10;
		
		System.out.println("NUM : " + NUM);
		
		
		
	}
}

